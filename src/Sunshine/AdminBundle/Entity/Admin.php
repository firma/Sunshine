<?php

namespace Sunshine\AdminBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Security\Core\User\AdvancedUserInterface;


/**
 * Admin
 *
 * @ORM\Table(name="sunshine_admin_admin")
 * @ORM\Entity(repositoryClass="Sunshine\AdminBundle\Repository\AdminRepository")
 */
class Admin implements AdvancedUserInterface, \Serializable
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    protected $id;

    /**
     * @var string
     *
     * @ORM\Column(name="username", type="string", length=25)
     */
    protected $username;

    /**
     * @var string
     *
     * @ORM\Column(name="password", type="string", length=64)
     */
    protected $password;

    /**
     * @var bool
     *
     * @ORM\Column(type="boolean")
     */
    protected $isActive;

    public function __construct()
    {
        $this->isActive = true;
    }

    public function __toString()
    {
        return (string) $this->getUsername();
    }

    /**
